int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
 * add - Adds two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The sum of the two integers
 */
int add(int a, int b)
{
return a + b;
}

/**
 * sub - Subtracts two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The result of the subtraction (a - b)
 */
int sub(int a, int b)
{
return a - b;
}

/**
 * mul - Multiplies two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The product of the two integers
 */
int mul(int a, int b)
{
return a * b;
}

/**
 * div - Divides two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The result of the division (a / b)
 */
int div(int a, int b)
{
return a / b;
}

/**
 * mod - Computes the modulus of two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The modulus of the two integers
 */
int mod(int a, int b)
{
return a % b;
}
